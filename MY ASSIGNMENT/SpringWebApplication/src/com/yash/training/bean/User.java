package com.yash.training.bean;

public class User {
	
	private String name;
	private long contact;
	
	public void setName(String name)
	{
		this.name = name;
	}

	public String getName() 
	{
		return name;
	}
	
	public void setContact(long contact) 
	{
		this.contact = contact;
	}
	
	public long getContact() 
	{
		return contact;
	}
}
