package com.ajaxtest.model;

public class Employee {
	
	private int emp_id;
	private String emp_name;
	private String emp_loc;
	private String emp_desig;
	private String emp_depart;
	
	public String getEmp_depart() {
		return emp_depart;
	}
	public void setEmp_depart(String emp_depart) {
		this.emp_depart = emp_depart;
	}
	public int getEmp_id() {
		return emp_id;
	}
	public void setEmp_id(int emp_id) {
		this.emp_id = emp_id;
	}
	public String getEmp_name() {
		return emp_name;
	}
	public void setEmp_name(String emp_name) {
		this.emp_name = emp_name;
	}
	public String getEmp_loc() {
		return emp_loc;
	}
	public void setEmp_loc(String emp_loc) {
		this.emp_loc = emp_loc;
	}
	public String getEmp_desig() {
		return emp_desig;
	}
	public void setEmp_desig(String emp_desig) {
		this.emp_desig = emp_desig;
	}

}
